	if(f.sname.value == ""){
		alert("학생이름을 입력해주세요");
		return f.sname.focus();
	}
	if(f.address.value == ""){
		alert("학생주소를 입력해주세요");
		return f.address.focus();
	}
	if(f.gender.value == ""){
		alert("학생성별을 입력해주세요");
		return f.gender.focus();
	}
	if(f.tel.value == ""){
		alert("전화번호를 입력해주세요");
		return f.tel.focus();
	}
	if(f.idnum.value == ""){
		alert("주민번호를 입력해주세요");
		return f.idnum.focus();
	}
